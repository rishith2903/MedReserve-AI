export const getDepartmentByName = (name: string): Department | undefined => {
  return departments.find(dept => dept.name.toLowerCase() === name.toLowerCase());
};

export const getDepartmentsByEmergency = (emergencyAvailable: boolean): Department[] => {
  return departments.filter(dept => dept.emergencyAvailable === emergencyAvailable);
};

export const getTotalDoctors = (): number => {
  return departments.reduce((total, dept) => total + dept.doctorCount, 0);
};

export const getTotalDepartments = (): number => {
  return departments.length;
};
